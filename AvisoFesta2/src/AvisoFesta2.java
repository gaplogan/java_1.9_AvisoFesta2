public class AvisoFesta2 {
    public static void main(String[] args) throws Exception {
        //declara variável inteira para controle do loop
        int j = 1; //inicializa variável de controle do loop
            
        while (j <= 5)
        {//início do loop
            //escreve a frase
            System.out.println("Hoje vai ter festa na casa da Fabi");
            //incrementa variável de controle do loop (contador)
            j++; // Mesmo que j = j + 1;
        }//fim do loop
    }
}
